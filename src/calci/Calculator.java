package calci;

public class Calculator {
	int num1 = 25;
	int num2 = 12;

	public void add() {
		System.out.println(num1 + num2);
	}

	public void substract() {
		System.out.println(num1 - num2);
	}

	public void multiply() {
		System.out.println(num1 * num2);
	}

	public void divide() {
		System.out.println(num1 / num2);
	}

}
